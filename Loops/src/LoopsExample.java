public class LoopsExample {
    public static void main(String[] args) {
        for(int i =0;i<=10; i++){
            System.out.println("i ="+ i);
        }
        for(int i=10;i>=1;i--){
            System.out.println("i ="+i);
        }
        int i=0;
        while(i<=10){
            System.out.println("i="+i);
            i++;
        }
        int j=10;
        do{
            System.out.println("j =" + j);
            j--;
        }while(j>=1);
    }

}












